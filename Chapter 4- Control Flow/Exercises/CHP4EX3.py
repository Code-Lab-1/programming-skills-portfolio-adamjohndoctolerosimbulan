#Assigning the string to a variable
AlienColor = 'Yellow'

#Using if statement, if color is Green prints the output below
if AlienColor == 'Green':
    print("You just earned 5 points!")
#elif color is yellow prints the output below    
elif AlienColor == 'Yellow':
    print("You just earned 10 points!")
#else prints the output below   
else:
    print("You just earned 15 points!")