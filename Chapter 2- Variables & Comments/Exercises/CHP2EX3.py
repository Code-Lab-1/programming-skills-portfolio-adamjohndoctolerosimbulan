#Assigning the variable
txt = "\tAdam John\n"

#Displays the txt
print("Normal:")
print(txt)

#Displays the lstrip() method
print("lstrip() Method:")
print(txt.lstrip())

#Displays the rstrip() method
print("rstrip() Method:")
print(txt.rstrip())

#Displays the strip() method
print("\nstrip() Method:")
print(txt.strip())